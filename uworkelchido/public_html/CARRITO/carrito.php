$('body').on('click', '.btnEliminar', function() {
    let idProducto = $(this).data('id');
    eliminarProducto(idProducto);
});

function eliminarProducto(idProducto) {
    let productos = JSON.parse(localStorage.getItem('productos'));
    let productosActualizados = productos.filter(producto => producto.id !== idProducto);
    localStorage.setItem('productos', JSON.stringify(productosActualizados));
    mostrarCarrito();
}

        function mostrarCarrito() {
    if (localStorage.getItem("productos") != null) {
        let array = JSON.parse(localStorage.getItem('productos'));
        if (array.length > 0) {
            $.ajax({
                url: 'ajax.php',
                type: 'POST',
                async: true,
                data: {
                    action: 'buscar',
                    data: array
                },
                success: function(response) {
                    console.log(response);
                    const res = JSON.parse(response);
                    let html = '';
                    res.datos.forEach(element => {
                        html += `
                            <tr>
                                <td>${element.id}</td>
                                <td>${element.nombre}</td>
                                <td>${element.precio}</td>
                                <td>1</td>
                                <td>${element.precio}</td>
                                <td><button class="btn btn-danger btnEliminar" data-id="${element.id}">Eliminar</button></td>
                            </tr>
                        `;
                    });
                    $('#tblCarrito').html(html);
                    $('#total_pagar').text(res.total);

                    // Verificar si el botón de PayPal ya existe
                    if ($('#paypal-button-container').children().length > 0) {
                        // Si existe, eliminarlo
                        $('#paypal-button-container').empty();
                    }

                    paypal.Buttons({
                        style: {
                            color: 'blue',
                            shape: 'pill',
                            label: 'pay'
                        },
                        createOrder: function(data, actions) {
                            return actions.order.create({
                                purchase_units: [{
                                    amount: {
                                        value: res.total
                                    }
                                }]
                            });
                        },
                        onApprove: function(data, actions) {
                            return actions.order.capture().then(function(details) {
                                alert('Transaction completed by ' + details.payer.name.given_name);
                            });
                        }
                    }).render('#paypal-button-container');
                },
                error: function(error) {
                    console.log(error);
                }
            });
        }
    }
}

